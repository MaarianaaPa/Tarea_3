public class Tarea3POO {

    public static void main(String[] args) {
        int repetir;
        for (repetir = 1; repetir <= 3; repetir++) {
            System.out.printf("%7s %6s %n", "|", "|");
        }
        System.out.println("______ ______ ______");
        for (repetir = 1; repetir <= 3; repetir++) {
            System.out.printf("%7s %6s %n", "|", "|");
        }
        System.out.println("______ ______ ______");
        for (repetir = 1; repetir <= 3; repetir++) {
            System.out.printf("%7s %6s %n", "|", "|");
        }
    }
}